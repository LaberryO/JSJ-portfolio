# 색깔 가져다 쓰는 용도

class Color:
    def black(self):
        return (0, 0, 0);

    def white(self):
        return (255, 255, 255);

    def lightGray(self):
        return (128, 128, 128);

    def gray(self):
        return (50, 50, 50);

    def red(self):
        return (255, 0, 0);

    def aqua(self):
        return (0, 255, 255);

    def green(self):
        return (0, 255, 0);

    def customYellow(self):
        return (255, 199, 30);

    def customGreen(self):
        return (168, 255, 108);
        
